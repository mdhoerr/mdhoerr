<h2>weekly web publication from September 1998 to October 2000</h2>
<p>This is a good example of my writing skills and the way I solicit and 
respond to feedback from visitors. Almost every week for two years, I posted 
three to five site reviews and a three to several paragraph feature article. 
The site also provided a site search (using Atomz). <a target="typeguide" href="/mbtypeguide/index.html">[show me]</a>
</p>
<p>I initially created this publication as a demonstration for The Mining Company 
(later to become About.com), to try to get a job there as a web guide. I also 
tried to make the site self-supporting by trying various affiliate programs, but 
the best I was able to achieve was recovery of the cost of maintaining the site 
(mainly ISP fees) and a "wage" of approximately 10 cents an hour. I didn't get 
a job at The Mining Company, but I did get a web developer position at the 
Energy Center of Wisconsin.</p>
<p>Since I created this site just as style sheets were starting out (IE 3 and 
Netscape 4), it uses a simple table-based layout and all formatting uses (mostly 
deprecated) html tags. I used a variety of methods to get feedback from my visitors,
ranging from pop-up feedback forms (from forkinthehead and other sources), to 
visitor tracking (SuperStats) to polls and regular questions of the month. 
However, I did not program these myself, other than the html form markup and 
occassionally setting values in perl scripts written by third parties.</p>
